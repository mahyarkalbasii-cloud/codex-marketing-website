function makeCategoryDescriptions(style: SalesStyleId) {
  return Object.fromEntries(
    CATEGORIES.filter((category) => !category.excludeFromPages).map((category) => {
      const description =
        style === "fast"
          ? `در ${category.faTitle}، فرصت‌های سریع معمولاً وقتی ارزش دارند که نیاز پروژه روشن شده و تیم فروش باید زود تماس بگیرد، قیمت بدهد و پیگیری را از دست ندهد.`
          : `در ${category.faTitle}، فروش مشاوره‌ای زمانی مهم می‌شود که تصمیم خرید قبل از لحظه اجرا شکل می‌گیرد و فروشنده باید اعتماد، شواهد فنی و رابطه حرفه‌ای بسازد.`;

      return [category.slug, description];
    }),
  );
}

